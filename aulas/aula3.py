# Assunto: Introdução aos tipos de dados

"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Julio Mendes')
print('Julio "Mendes"')

# Aspas duplasd
print("Julio Mendes")
print("Julio 'Mendes'")
# Escapes
print("Julio \"Mendes") # A \ faz o interpretador ignorar o próximo carácter

# r
print(r'Julio \'Mendes\'') # O r faz tudo aparecer tudo 